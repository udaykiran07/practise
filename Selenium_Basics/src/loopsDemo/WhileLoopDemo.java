package loopsDemo;

public class WhileLoopDemo {

	public static void main(String[] args) {
		int count = 10;
		
		System.out.println("Begining of While Lopp");
		
		while(count<=20) {
			System.out.println("The Values are " +count);
			
			count++;
		}
		
		System.out.println("End of While Loop");
	}

}
